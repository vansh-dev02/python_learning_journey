#arithmetic operation

x=10
y=5

print(x+y)#addition
print(x-y)#subtraction
print(x/y)#divison
print(x*y)#multiplication
print(x%y)#remainder
print(x**y)#power


#comparison operation

#1 is true and 0 is false

print(x==y)
print(x>y)
print(x<y)
print(x>=y)
print(x<=y)

#logical operations
#and or not

print(x>y and x<y)#if both true output true
print(x>y or x<y)#if even 1 true output true
print(not(x>y))#convert true to false and false to true

